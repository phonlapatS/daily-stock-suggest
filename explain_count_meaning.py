print("\n**วิธีการคำนวณ Count:**")
print("""
1. ระบบ scan ประวัติทั้งหมด (เช่น 2500 bars)
2. หา pattern ที่ตรงกัน (เช่น "++-", "--+")
3. เก็บ return ของวันถัดไป (N+1) ทุกครั้งที่เจอ pattern
4. Count = จำนวนครั้งที่เจอ pattern นี้ในประวัติทั้งหมด

ตัวอย่าง:
- Pattern "++-" เกิดขึ้น 100 ครั้งในประวัติ
- Count = 100 (ไม่ใช่จำนวนที่ทายถูก)
- ถ้าทายถูก 60 ครั้ง → Prob% = 60%
""")

print("\n**สูตรคำนวณ:**")
print("""
Count = จำนวน matches ทั้งหมด (ทั้งที่ทายถูกและผิด)
Prob% = (Wins / Count) × 100
Wins = จำนวนครั้งที่ทายถูก

ตัวอย่าง:
- Count = 100
- Wins = 60
- Prob% = (60 / 100) × 100 = 60%
""")

print("\n**สรุป:**")
print("""
1. Count = จำนวน matches ทั้งหมด (ไม่ใช่แค่ที่ทายถูก)
2. Prob% = Win Rate = (Wins / Count) × 100
3. Count ไม่ได้มาจาก 2500 bar โดยตรง แต่มาจากจำนวน matches ของ pattern ที่เจอในประวัติ

ตัวอย่าง:
- ถ้า Count = 100 → หมายความว่า pattern นี้เกิดขึ้น 100 ครั้งในประวัติ
- ถ้า Prob% = 60% → หมายความว่า 60 ครั้งทายถูก, 40 ครั้งทายผิด
- Wins = 60, Losses = 40
""")

